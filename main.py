# draw a christmus tree
from turtle import *
speed("fastest")
penup()
color("brown")
setposition(25, -350)
pendown()
begin_fill()
left(90)
forward(300)
left(90)
forward(50)
left(90)
forward(300)
left(90)
forward(50)
end_fill()
penup()
color("green")
setposition(200, -300)
pendown()
begin_fill()
setposition(-200, -300)
setposition(0, 200)
end_fill()
penup()
color("red")
setposition(200, -300)

setposition(0, 200)
pendown()
begin_fill()
color("gold")
for side in range(5):
        forward(15)
        right(90)
        forward(15)
        right(72 - 90)
end_fill()

pendown()
exitonclick()
